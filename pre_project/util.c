double l2norm(double f[3][3])
{
  int i, j;
  double s;

  s = 0;
  for (i = 0; i < 3; i++) {
    for (j = 0; j < 3; j++) {
      s += f[i][j] * f[i][j];
    }
  }
  return sqrt(s/9.0);
}

